package org.example.browser.CSS;


public class Specifity{
    private int a;
    private int b;
    private int c;

    public Specifity(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }


    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

}
